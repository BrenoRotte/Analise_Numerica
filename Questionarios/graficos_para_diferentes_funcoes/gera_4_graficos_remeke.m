%---------------------------------------
ya = 9.*sin(x.^1)*9.*cos(x.^10)*8.*(t(1,2).^1)*8.*x.^10;
%----------------------------------------
yb = 9.*sin(x.^1)*9.*cos(x.^10)*8.*(t(1,3).^1)*8.*x.^10;
%----------------------------------------
yc =9.*sin(x.^1)*9.*cos(x.^10)*8.*(t(1,4).^1)*8.*x.^10;
%----------------------------------------
plot(x,y,'+m',x,ya,'+m',x,yb,'+m',x,yc,'+m');